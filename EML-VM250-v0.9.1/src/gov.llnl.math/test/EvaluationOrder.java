public class EvaluationOrder
{
  static int foo(int i)
  {
    System.out.println("i=" + i);
    return i;
  }

  static public void main(String[] args)
  {
    int i = 0;
    int j = foo(i) + foo(++i) + foo(i);
    System.out.println("j=" + j);

  }
}


