package gov.llnl.ernie.io;

/**
 * Exception thrown when unable to retrieve a record from a RecordSource.
 *
 * @author nelson85
 */
public class RecordException extends Exception
{
  private static final long serialVersionUID = gov.llnl.utility.UUIDUtilities.createLong("RecordException-v1");

  public RecordException(String reason)
  {
    super(reason);
  }

  public RecordException(String reason, Exception cause)
  {
    super(reason, cause);
  }

}


