package gov.llnl.ernie.data;

/**
 * Used to specify a virtual sensor.
 *
 * @author nelson85
 */
public enum SensorPosition
{
  TOP(0), BOTTOM(1), LEFT(2), RIGHT(3), ALL(4);
  private final int value;

  private SensorPosition(int value)
  {
    this.value = value;
  }

  public int getValue()
  {
    return value;
  }

}


