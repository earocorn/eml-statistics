package gov.llnl.ernie.analysis;

/**
 * Exception for acceptable failures with the analysis.
 *
 * This should be used for any handleable failures that occur during the
 * analysis. This will be caught and will then result in the analysis going to
 * fallback.
 *
 * @author nelson85
 */
public class AnalysisException extends Exception
{
  private static final long serialVersionUID = gov.llnl.utility.UUIDUtilities.createLong("AnalysisException-v1");

  public AnalysisException(String reason)
  {
    super(reason);
  }
}


