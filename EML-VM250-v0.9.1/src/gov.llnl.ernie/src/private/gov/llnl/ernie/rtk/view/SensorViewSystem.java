package gov.llnl.ernie.rtk.view;

import gov.llnl.ernie.data.SensorView;
import java.util.ArrayList;
import java.util.Iterator;
import java.util.List;

/**
 * Collection of sensor views representing a number of sensors.
 *
 * This will be the front end for simulating a detector encounter.
 *
 * @author nelson85
 */
public class SensorViewSystem implements Iterable<SensorView>
{
  List<SensorView> sensors=new ArrayList<>();

  @Override
  public Iterator<SensorView> iterator()
  {
    return sensors.iterator();
  }

}


