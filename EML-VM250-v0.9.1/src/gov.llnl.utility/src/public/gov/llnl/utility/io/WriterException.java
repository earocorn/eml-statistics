public class WriterException extends Exception
{
  public WriterException(String msg)
  {
    super(msg);
  }

  public WriterException(String msg, Throwable ex)
  {
    super(msg, ex);
  }

  public WriterException(Throwable ex)
  {
    super(ex);
  }
}


