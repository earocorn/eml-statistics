package gov.llnl.utility.matlab;

import java.util.ArrayList;

/**
 *
 * @author seilhan3
 */
public class DoubleAggregator extends ArrayList<Double>
{
  public double[] getPrimitiveArray()
  {
    double[] foo = new double[this.size()];
    int i = 0;
    for (Double d : this)
    {
      foo[i++] = d;
    }
    return foo;
  }
}


