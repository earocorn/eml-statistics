package gov.llnl.utility.internal.xml.bind.readers;

import gov.llnl.utility.UtilityPackage;
import gov.llnl.utility.io.ReaderException;
import gov.llnl.utility.xml.bind.ObjectReader;
import gov.llnl.utility.xml.bind.Reader;

/**
 *
 * @author nelson85
 */
@Reader.Declaration(
        pkg = UtilityPackage.class,
        name = "float",
        referenceable = true,
        contents = Reader.Contents.TEXT)
@Reader.TextContents(base = "xs:double")
public class FloatContents extends ObjectReader<Float>
{

  @Override
  public Class<Float> getObjectClass()
  {
    return Float.class;
  }

  @Override
  @TextContents(base = "xs:double")
  public Float contents(String textContents) throws ReaderException
  {
    return Float.parseFloat(textContents);
  }

}


