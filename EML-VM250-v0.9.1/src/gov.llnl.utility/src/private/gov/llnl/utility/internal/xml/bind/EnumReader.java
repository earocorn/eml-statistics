package gov.llnl.utility.internal.xml.bind;

import gov.llnl.utility.UtilityPackage;
import gov.llnl.utility.io.ReaderException;
import gov.llnl.utility.xml.bind.ObjectReader;
import gov.llnl.utility.xml.bind.Reader;

/**
 *
 * @author nelson85
 */
@Reader.Declaration(name = "enum", pkg = UtilityPackage.class, contents = ObjectReader.Contents.TEXT)
public class EnumReader<T extends Enum> extends ObjectReader<T>
{
  Class<T> cls;

  public EnumReader()
  {
  }

  public EnumReader(Class<T> cls)
  {
    this.cls = cls;
  }

  @Override
  public T contents(String textContents) throws ReaderException
  {
    return (T) Enum.valueOf(cls, textContents);
  }

  @Override
  public Class getObjectClass()
  {
    return cls;
  }
}


